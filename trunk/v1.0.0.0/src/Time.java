
public class Time {
	private int hours;
	private int mins;
	
	public Time(){
		
	}
	
	public int getHours(){
		return this.hours;
	}
	
	public int getMin(){
		return this.mins;
	}
	
	public void setHours(int hours){
		this.hours = hours;
	}
	
	public void setMins(int mins){
		this.mins = mins;
	}
}
