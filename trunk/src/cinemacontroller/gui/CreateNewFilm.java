    public Film createFilmFromDetails(GregorianCalendar start_date, GregorianCalendar expire_date){

        String title = this.jTextField1.getText();
        String director = this.jTextField2.getText();
        String BBFC_rating = this.jComboBox1.getSelectedItem().toString();
        String type = this.type_combo.getSelectedItem().toString();

        GregorianCalendar length = new GregorianCalendar();
        length.setTimeInMillis(0);
        length.set(Calendar.HOUR_OF_DAY, Integer.parseInt(this.jTextField7.getText()));
        length.set(Calendar.MINUTE, Integer.parseInt(this.jTextField8.getText()));

        return new Film(this.controller.generateID(), title, director, BBFC_rating, length, new GregorianCalendar(), expire_date, selected_color_string, Film.TYPE.valueOf(type));
    }


