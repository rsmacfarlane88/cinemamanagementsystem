    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        GregorianCalendar expire_date = new GregorianCalendar();
        expire_date.setTimeInMillis(0);
        expire_date.set(Calendar.DAY_OF_MONTH, Integer.parseInt(this.expire_day.getText()));
        expire_date.set(Calendar.MONTH, Integer.parseInt(this.expire_month.getText()));
        expire_date.set(Calendar.YEAR, Integer.parseInt(this.expire_year.getText()));
        
        GregorianCalendar start_date = new GregorianCalendar();
        start_date.set(Calendar.MONTH, start_date.get(Calendar.MONTH) + 1);

        // 1209600000 = 2 weeks in milliseconds
        if((expire_date.getTimeInMillis() - start_date.getTimeInMillis()) >= 1209600000){
            Film new_film = this.createFilmFromDetails(start_date, expire_date);
        
            this.controller.film_controller.addFilm(new_film);
            this.window.updateStatus("New Film Added to Database.");
            
            try {
                this.controller.film_controller.film_database_controller.addFilm(new_film);
                this.controller.historical_controller.addFilm(new_film);
            }catch(Exception e){
                System.out.println(e);
            }

            this.dispose();
        }else{
            JOptionPane.showMessageDialog(null, "Sorry but a film must be booked for at least two weeks.", "Minimum Booking too short", JOptionPane.WARNING_MESSAGE);
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

