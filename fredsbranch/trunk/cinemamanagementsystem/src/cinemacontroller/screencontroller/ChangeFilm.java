    public void fillListCurrentPeriod(){
		
    	LoadCurrentFilm currentFilm = new LoadCurrentFilm();
    	currentFilm.start(screeningDay, use.getDateEndPeriod());
    	
        MySqlController connection;
        ResultSet result;

		try {
			connection = MySqlController.getInstance();
			result= connection.getData("SELECT M.NAME FROM MOVIES M, TEMPEXPECTEDBOOKING T WHERE M.ID_MOVIE=T.ID_MOVIE ORDER BY M.NAME");

			while (result.next()) {
				model3.addElement(result.getString(1));
			}
			
		} catch (SQLException e) {
			e.printStackTrace();
		}
		
		jList3.setModel(model3);
	
    }

